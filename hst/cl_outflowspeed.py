#Calculation of momentum input in dynes using dp/dt = 5*10^(33) * (SFR in solar masses per year)
import numpy as np
#SFR in order of J0826,J0901,J0905,J0944,J1107,J1219,J1341,J1506,J1558,J1613,J2116,J2140
SFR = [287,119,339,184,183,269,515,638,98,429,149,302]
momin = np.zeros(12)
for w in range(0,12):
    momin[w] = 5*10**(33)*SFR[w]

#Calculating Outflow Speed from Heckman 2011 formula
p = [1.435*(10**(36)),5.95*(10**(35)),1.695*(10**(36)),9.2*(10**(35)),9.15*(10**(35)),1.345*(10**(36)),2.575*(10**(36)),3.19*(10**(36)),4.9*(10**(35)),2.145*(10**(36)),7.45*(10**(35)),1.51*(10**(36))]
r = [[84,134],[175,149],[86,83],[158,74],[62,179],[164,239],[170,81],[77,108],[215,1032],[560,886],[98,178],[91,189]]
N = 1


v=np.zeros([12,2])
for w in range(0,12):
    for i in range(0,2):
        v[w][i] = 1800 * (p[w]/(10**35))**(1/2) * (2/3)**(-1/2) * (r[w][i]/(100))**(-1/2) * N**(-1/2)
